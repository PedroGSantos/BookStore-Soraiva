    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisarActionPerformed
        try{
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo ID_Livro");
            txtid.requestFocus();
        }
            LivroSALVA Coleta = DAO.pesquisar(Integer.parseInt(txtid.getText()));
        
        if(Coleta==null){
            JOptionPane.showMessageDialog(this, "Não existem registros com esse ID");
            txtid.setText("");
       }
        else{
            txtnome.setText(Coleta.getNome());
            txtidade.setText(Integer.toString(Coleta.getIdade()));
            txtcpf.setText(Integer.toString(Coleta.getCPF()));
            txtrg.setText(Integer.toString(Coleta.getRG()));
            txtano.setText(Integer.toString(Coleta.getAno_Lançamento()));
            cbogenero.setSelectedItem(Coleta.getGenero_Livro());
            txteditora.setText(Coleta.getEditora());
            
            txtnomelivro.setText(Coleta.getNome_Livro());
            txtautor.setText(Coleta.getAutor());
        }
        }catch(Exception erro){
            JOptionPane.showMessageDialog(this, "Erro"+erro.getMessage());
        }
    }//GEN-LAST:event_btnpesquisarActionPerformed

