public boolean validarCampos(){
    if(txtnome.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Nome");
      txtnome.requestFocus();
      return false;
    }
    if(txtidade.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Idade");
      txtidade.requestFocus();
      return false;
    }
     if(txtcpf.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo CPF");
      txtcpf.requestFocus();
      return false;
    }
      if(txtrg.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo RG");
      txtrg.requestFocus();
      return false;
    }
       if(txtano.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Ano");
      txtano.requestFocus();
      return false;
    }
        if(cbogenero.getSelectedItem().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Genero do livro");
      cbogenero.requestFocus();
      return false;
    }
         if(txteditora.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Editora");
      txteditora.requestFocus();
      return false;
    }
          if(txtid.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo ID livro");
      txtid.requestFocus();
      return false;
    }
           if(txtnomelivro.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Nome livro");
      txtnomelivro.requestFocus();
      return false;
    }
            if(txtautor.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Preencha o campo Autor");
      txtautor.requestFocus();
      return false;
    }
           
    return true;
}
public boolean preencher(){
    Coleta = new LivroSALVA();
    Coleta.setNome(txtnome.getText());
    Coleta.setIdade(Integer.parseInt(txtidade.getText()));
    Coleta.setCPF(Integer.parseInt(txtcpf.getText()));
    Coleta.setRG(Integer.parseInt(txtrg.getText()));  
    Coleta.setAno_Lançamento(Integer.parseInt(txtano.getText()));
    Coleta.setGenero_Livro(Integer.toString(cbogenero.getSelectedIndex()));
    Coleta.setEditora(txteditora.getText());
    Coleta.setID_Livro(Integer.parseInt(txtid.getText()));
    Coleta.setNome_Livro(txtnomelivro.getText());
    Coleta.setAutor(txtautor.getText());
    
    return true;
}

